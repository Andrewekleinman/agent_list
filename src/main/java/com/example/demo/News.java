public class News {
    public String id;
    public String title;
    public String description;
    public String image;
    public String video;

    public News(String id, String title, String description, String image, String video){
        this.id=id;
        this.title=title;
        this.description=description;
        this.image=image;
        this.video=video;
    }
}
